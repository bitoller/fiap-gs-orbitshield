CREATE TABLE OS_USERS (
    USER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NAME VARCHAR2(120 CHAR) NOT NULL,
    EMAIL VARCHAR2(180 CHAR) NOT NULL,
    PASSWORD_HASH VARCHAR2(255 CHAR) NOT NULL,
    ROLE VARCHAR2(30 CHAR) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_OS_USERS PRIMARY KEY (USER_ID),
    CONSTRAINT UK_OS_USERS_EMAIL UNIQUE (EMAIL),
    CONSTRAINT CK_OS_USERS_ROLE CHECK (ROLE IN ('Admin', 'Engineer', 'SatelliteDevice'))
);

CREATE TABLE OS_SATELLITES (
    SATELLITE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    CODE VARCHAR2(40 CHAR) NOT NULL,
    NAME VARCHAR2(120 CHAR) NOT NULL,
    ORBIT_STATUS VARCHAR2(30 CHAR) DEFAULT 'Nominal' NOT NULL,
    FUEL_PERCENTAGE NUMBER(3) NOT NULL,
    SOLAR_ENERGY_PERCENTAGE NUMBER(3) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_OS_SATELLITES PRIMARY KEY (SATELLITE_ID),
    CONSTRAINT UK_OS_SATELLITES_CODE UNIQUE (CODE),
    CONSTRAINT CK_OS_SATELLITES_STATUS CHECK (ORBIT_STATUS IN ('Nominal', 'Warning', 'Emergency')),
    CONSTRAINT CK_OS_SATELLITES_FUEL CHECK (FUEL_PERCENTAGE BETWEEN 0 AND 100),
    CONSTRAINT CK_OS_SATELLITES_SOLAR CHECK (SOLAR_ENERGY_PERCENTAGE BETWEEN 0 AND 100)
);

CREATE TABLE OS_DEBRIS_OBJECTS (
    DEBRIS_OBJECT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    CODE VARCHAR2(60 CHAR) NOT NULL,
    ESTIMATED_SIZE_METERS NUMBER(8,2) NOT NULL,
    RISK_LEVEL VARCHAR2(30 CHAR) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_OS_DEBRIS_OBJECTS PRIMARY KEY (DEBRIS_OBJECT_ID),
    CONSTRAINT UK_OS_DEBRIS_OBJECTS_CODE UNIQUE (CODE),
    CONSTRAINT CK_OS_DEBRIS_SIZE CHECK (ESTIMATED_SIZE_METERS >= 0),
    CONSTRAINT CK_OS_DEBRIS_RISK CHECK (RISK_LEVEL IN ('Low', 'Medium', 'High', 'Critical'))
);

CREATE TABLE OS_ORBITAL_ELEMENTS (
    ORBITAL_ELEMENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    SATELLITE_ID NUMBER NOT NULL,
    NAME VARCHAR2(120 CHAR) NOT NULL,
    TLE_LINE1 VARCHAR2(120 CHAR) NOT NULL,
    TLE_LINE2 VARCHAR2(120 CHAR) NOT NULL,
    EPOCH TIMESTAMP NOT NULL,
    SOURCE VARCHAR2(60 CHAR) DEFAULT 'CelesTrak' NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_OS_ORBITAL_ELEMENTS PRIMARY KEY (ORBITAL_ELEMENT_ID),
    CONSTRAINT FK_OS_TLE_SATELLITE FOREIGN KEY (SATELLITE_ID) REFERENCES OS_SATELLITES (SATELLITE_ID),
    CONSTRAINT CK_OS_TLE_LINE1 CHECK (TLE_LINE1 LIKE '1 %'),
    CONSTRAINT CK_OS_TLE_LINE2 CHECK (TLE_LINE2 LIKE '2 %')
);

CREATE TABLE OS_CONJUNCTION_EVENTS (
    CONJUNCTION_EVENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    SATELLITE_ID NUMBER NOT NULL,
    DEBRIS_OBJECT_ID NUMBER NOT NULL,
    DISTANCE_KM NUMBER(10,2) NOT NULL,
    PROBABILITY NUMBER(5,2) NOT NULL,
    COLLISION_RISK NUMBER(1) NOT NULL,
    DETECTED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    STATUS VARCHAR2(30 CHAR) DEFAULT 'Open' NOT NULL,
    CONSTRAINT PK_OS_CONJUNCTION_EVENTS PRIMARY KEY (CONJUNCTION_EVENT_ID),
    CONSTRAINT FK_OS_CONJ_SATELLITE FOREIGN KEY (SATELLITE_ID) REFERENCES OS_SATELLITES (SATELLITE_ID),
    CONSTRAINT FK_OS_CONJ_DEBRIS FOREIGN KEY (DEBRIS_OBJECT_ID) REFERENCES OS_DEBRIS_OBJECTS (DEBRIS_OBJECT_ID),
    CONSTRAINT CK_OS_CONJ_DISTANCE CHECK (DISTANCE_KM >= 0),
    CONSTRAINT CK_OS_CONJ_PROBABILITY CHECK (PROBABILITY BETWEEN 0 AND 100),
    CONSTRAINT CK_OS_CONJ_RISK CHECK (COLLISION_RISK IN (0, 1)),
    CONSTRAINT CK_OS_CONJ_STATUS CHECK (STATUS IN ('Open', 'Mitigated', 'Closed'))
);

CREATE TABLE OS_MANEUVER_LOGS (
    MANEUVER_LOG_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    SATELLITE_ID NUMBER NOT NULL,
    CONJUNCTION_EVENT_ID NUMBER NULL,
    ACTION VARCHAR2(240 CHAR) NOT NULL,
    SERVO_ANGLE NUMBER(3) NOT NULL,
    THRUST_LEVEL NUMBER(8,2) NULL,
    SOURCE VARCHAR2(40 CHAR) DEFAULT 'ESP32' NOT NULL,
    EXECUTED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_OS_MANEUVER_LOGS PRIMARY KEY (MANEUVER_LOG_ID),
    CONSTRAINT FK_OS_MANEUVER_SATELLITE FOREIGN KEY (SATELLITE_ID) REFERENCES OS_SATELLITES (SATELLITE_ID),
    CONSTRAINT FK_OS_MANEUVER_CONJUNCTION FOREIGN KEY (CONJUNCTION_EVENT_ID) REFERENCES OS_CONJUNCTION_EVENTS (CONJUNCTION_EVENT_ID) ON DELETE SET NULL,
    CONSTRAINT CK_OS_MANEUVER_SERVO CHECK (SERVO_ANGLE BETWEEN 0 AND 180)
);

CREATE TABLE OS_SENSOR_READINGS (
    SENSOR_READING_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    SATELLITE_ID NUMBER NOT NULL,
    TEMPERATURE_CELSIUS NUMBER(8,2) NOT NULL,
    RADIATION_LEVEL NUMBER(8,2) NOT NULL,
    BATTERY_VOLTAGE NUMBER(8,2) NOT NULL,
    SIMULATED_GRAVITY NUMBER(8,4) NULL,
    SIMULATED_THRUST NUMBER(8,2) NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_OS_SENSOR_READINGS PRIMARY KEY (SENSOR_READING_ID),
    CONSTRAINT FK_OS_SENSOR_SATELLITE FOREIGN KEY (SATELLITE_ID) REFERENCES OS_SATELLITES (SATELLITE_ID),
    CONSTRAINT CK_OS_SENSOR_RADIATION CHECK (RADIATION_LEVEL >= 0),
    CONSTRAINT CK_OS_SENSOR_BATTERY CHECK (BATTERY_VOLTAGE >= 0)
);

CREATE INDEX IX_OS_CONJ_SATELLITE_DATE ON OS_CONJUNCTION_EVENTS (SATELLITE_ID, DETECTED_AT);
CREATE INDEX IX_OS_SENSOR_SATELLITE_DATE ON OS_SENSOR_READINGS (SATELLITE_ID, CREATED_AT);
CREATE INDEX IX_OS_MANEUVER_SATELLITE_DATE ON OS_MANEUVER_LOGS (SATELLITE_ID, EXECUTED_AT);

